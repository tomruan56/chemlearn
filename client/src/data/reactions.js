// Search function
export function searchReactions(query) {
  if (!query || !query.trim()) return []
  const q = query.trim()

  // Exact and partial formula/symbol matching
  const qLower = q.toLowerCase()
  const qClean = q.replace(/\s/g, '')

  return reactions.filter(r => {
    // Match element symbols (e.g. "Fe", "Na")
    const elementMatch = r.elements.some(el =>
      el.toLowerCase() === qLower
    )
    // Match compound formulas in involves list (e.g. "H2SO4", "NaOH")
    const formulaMatch = r.involves.some(f =>
      f.toLowerCase() === qClean.toLowerCase() ||
      f.toLowerCase().includes(qClean.toLowerCase())
    )
    // Match equation text
    const eqMatch = r.equation.toLowerCase().replace(/\s/g, '').includes(qClean.toLowerCase())

    return elementMatch || formulaMatch || eqMatch
  })
}
